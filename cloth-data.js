// function.
//
// ---- THE `verified` FLAG ----
//
// `verified: true`  — a real bunch from the mill's published range.
//   Carries `bunch` and whatever of `composition` / `weight` the mill
//   actually publishes, plus a `source` URL.
//
// `verified: false` — built to the mill's documented house style, and
//   named descriptively ("Charcoal Worsted Twill") rather than with an
//   invented bunch name. Carries NO composition, weight or bunch,
//   because a plausible-looking figure next to genuinely researched
//   mill data is worse than no figure at all.
//
// verify/audit.js enforces that rule, so the distinction cannot erode
// as the library grows. To promote a cloth: research it, add bunch +
// spec + source, flip the flag.
//
// ---- FIELDS ----
//
//   key            unique id, also the seed for the tile's texture
//   name           display name
//   mill           display name of the house
//   millPath       path into Cloth Origins — MUST resolve (audited)
//   guidePath      path to the weave's guide topic, where one fits
//   weave          one of WEAVE_GROUNDS in weave-engine.js
//   pattern        one of WEAVE_OVERLAYS in weave-engine.js
//   colour_family  filter facet: charcoal navy grey blue brown green tan cream black
//   weight_class   filter facet: light midweight heavy
//   ground         base colour hex
//   overlay        { type, colour, pitch, overcheck } — omit for plain cloths
